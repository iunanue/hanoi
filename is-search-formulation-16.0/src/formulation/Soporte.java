package formulation;

public class Soporte {
	
	private Integer soporte;
	private Integer disco;
	public Soporte(Integer soporte, Integer disco) {
		this.soporte = soporte;
		this.disco = disco;
	}
	public Integer getSoporte() {
		return soporte;
	}
	public void setSoporte(Integer soporte) {
		this.soporte = soporte;
	}
	public Integer getDisco() {
		return disco;
	}
	public void setDisco(Integer disco) {
		this.disco = disco;
	}
	
}
